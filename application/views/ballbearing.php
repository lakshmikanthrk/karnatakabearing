	<h1>Ball Bearing</h1>
	<p>Ball bearing, one of the members of the class of rolling, or so-called antifriction, bearings. The function of a ball bearing is to connect two machine members that move relative to one another in such a manner that the frictional resistance to motion is minimal. In many applications one of the members is a rotating shaft and the other a fixed housing.</p>
	<p> It achieves radial and axial load by using at least two races to contain the balls and transmit the loads through the balls. In most applications, one race is stationary and the other is attached to the rotating assembly. As one of the bearing races rotates it causes the balls to rotate as well. Because the balls are rolling they have a much lower coefficient of friction than if two flat surfaces were sliding against each other.</p>
	
