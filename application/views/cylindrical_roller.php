				<p>Single Row Cylindrical Roller Bearing is designed to support heavy radial loads. These roller bearings consist of an Outer Race, Bronze or Steel Cage, a complement of rollers, and usually an Inner Race.</p>
				<p>The rollers are guided by the ribs of the inner or outer ring. The inner and outer rings can be separated to facilitate assembly, and both can be fit with shaft or housing tightly. If there are no ribs, either the inner or the outer ring can move freely in the axial direction.</p>
				<p>In the case where there is a rib, the bearing can bear a slight axial load between the end of the rollers and the rib.</p>
				<p>Types NU, N, NNU, and NN are suitable as free-end bearings. Types of NJ and NF can sustain limited axial loads in one direction. Types NH and NUP can be used as fixed-end bearings.</p>
				<p> NH-type cylindrical roller bearings consist of the NJ-type cylindrical roller bearings and HJ-type L-shaped thrust collars.</p>
				<p>The inner ring loose rib of a NUP-type cylindrical roller bearing should be mounted so that the marked side is on the outside.</p>
